package guia2;
import javax.swing.JOptionPane;

public class ejercice3 {
	
	public static void main(String[]arg) {
	
		

        String valor1 = JOptionPane.showInputDialog("Ingrese el primer valor a sumar");
        
        int valorA = Integer.parseInt(valor1);
        
        String valor2= JOptionPane.showInputDialog("Ingrese el segundo valor a sumar");
        
        int valorB = Integer.parseInt(valor2);
		
		
        int formula=valorA+valorB;

		JOptionPane.showConfirmDialog(null, "El Total de la suma de los dos valores es: " + formula );
	}
}
